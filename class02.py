# Arithmetic operators.
num1 = 10
num2 = 30 
result = num1 + num2
print(result)

result = num2 - num1
print(result)

result = num1 * num2
print(result)

result = num2 / num1
print(result)

result = num1 % num2
print(result)

num3,num4 = 2,3
result = num3 ** num4
print(result)

result = num3 // num4
print(result)


# comparison operator

result = num1 == num2
print(result)

result = num2 >= num1
print(result)

result = num1 <= num2
print(result)

result = num1 != num2
print(result)

# Logical operator
result = num1 > num2 and num1 < num2 
print(result)

result = num1 > num2 or num4 > num3
print(result)

result = not(num1 > num2 or num4 > num3)
print(result)

